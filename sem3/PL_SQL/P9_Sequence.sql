INSERT INTO CUSTOMER VALUES (S2.NEXTVAL, 'SANIKA', 'KURLA');
INSERT INTO CUSTOMER VALUES (S2.NEXTVAL, 'SANIKA', 'KURLA');

-- View all records
SELECT * FROM CUSTOMER;

--5. Drop the sequence S2 (Q4) after use
DROP SEQUENCE S2;
