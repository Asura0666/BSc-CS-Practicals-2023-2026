INSERT INTO CUSTOMER VALUES (S1.NEXTVAL, 'LIPA', 'VIDYAVIHAR');

-- View all records
SELECT * FROM CUSTOMER;


-- 4. Create SEQUENCE S2 With No Cycle and Insert Multiple Values
CREATE SEQUENCE S2
  MAXVALUE 4
  CACHE 2
  NOCYCLE;

-- Insert values using sequence S2
INSERT INTO CUSTOMER VALUES (S2.NEXTVAL, 'SANIKA', 'KURLA');
INSERT INTO CUSTOMER VALUES (S2.NEXTVAL, 'SANIKA', 'KURLA');
